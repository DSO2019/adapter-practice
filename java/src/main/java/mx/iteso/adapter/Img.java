package mx.iteso.adapter;

/**
 * Class Img.
 */
public class Img {
    /**
     * Url.
     */
    private String url;
    /**
     * @param ur url.
     */
    public Img(final String ur) {
        this.url = ur;
    }
    /**
     * @return url.
     */
    public final String getUrl() {
        return this.url;
    }
}
